    private void btnBayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBayarActionPerformed
        // TODO add your handling code here:
        if(txtBayar.getText().equalsIgnoreCase(""))
            JOptionPane.showMessageDialog(this, "maaf anda belum membayar");
        else if((Integer.parseInt(txtBayar.getText()) - tempTotal) >= 0){
            tempTotal = Integer.parseInt(txtBayar.getText()) - tempTotal;
            labelKembalian.setText(String.valueOf(tempTotal));
            
            //====================insert data transaksi + detailnya=======================\\
            if (!T.getDT().isEmpty()) {
            for (DetilTransaksi DT : T.getDT()) {
                System.out.println(DT.getJumlah());
                }
            }
            TC.insertTransaksi(T);
        
            JOptionPane.showMessageDialog(this, "terima kasih telah datang di restoran kenthud");
            
            FormTransaksiUtama formTrans = new FormTransaksiUtama();
            formTrans.show();

            this.dispose();
        }else{
            JOptionPane.showMessageDialog(this, "maaf uang anda kurang");
            txtBayar.setText("");
        }
    }//GEN-LAST:event_btnBayarActionPerformed

