    private void btnTambahDetilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahDetilActionPerformed
        double tempHarga, tempHargaPerDetil;
        int tempJumlah, tempMenu;
        String namaMenu = cmbMenu.getSelectedItem().toString();
        Menu m = new Menu(MC.getIdByName(namaMenu), namaMenu);
        
        idDetilTransaksi = idDetilTransaksi + DTC.getNextId();
        DT = new DetilTransaksi(idDetilTransaksi, m, Integer.parseInt(txtJumlah.getText()), T.getId());
        DTList.add(DT);

        T.setDT(DTList);

        System.out.println("\nBerhasil dimasukan");
        
        addTableDetil();
        
        //=====================untuk  transaksi pembayaran=============================\\
        tempMenu = DT.getMenu().getID();
        tempHarga = DTC.getHargaMenu(tempMenu);
        tempJumlah = Integer.parseInt(txtJumlah.getText());
        
        tempHargaPerDetil = tempHarga * tempJumlah;
        tempTotal = tempTotal + tempHargaPerDetil;
        
        labelTotalHarga.setText(String.valueOf(tempTotal));
    }//GEN-LAST:event_btnTambahDetilActionPerformed

