    private void btnHapusDetilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusDetilActionPerformed
        // TODO add your handling code here:
        if(this.selectedRow == null)
            JOptionPane.showMessageDialog(null, "Silahkan pilih data yang hendak dihapus");
        else{
            String Nama = "test";
            int option = JOptionPane.showConfirmDialog(null, "Yakin hapus data ?", "hapus data" + Nama, JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE );
            if(option == JOptionPane.OK_OPTION){
                DTC.DeleteDetilTransaksi(this.selectedId);
                ShowTableDetil();
                clearTxtDetil();
            }
            else if(option == JOptionPane.CANCEL_OPTION){
                ShowTableDetil();
                clearTxtDetil();
            }
        }
    }//GEN-LAST:event_btnHapusDetilActionPerformed

