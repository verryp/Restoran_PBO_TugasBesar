    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed

        Pengguna P = new Pengguna(PC.getNextId(), txtNama.getText(), txtNamaPengguna.getText(), txtKataSandi.getText());
        Peran Pe = new Peran(cmbPeran.getSelectedIndex(), cmbPeran.getSelectedItem().toString());
        P.setPeran(Pe);

        try {
            if (btnTambah.getText() == "Tambah") {
                PC.insertPengguna(P);
                cekUsername(P.getUsername());
                cekPassword();
                clearForm();
                ShowTable();
            } else {
                P.setId(this.selectedId);
                PC.updatePengguna(P);
                btnTambah.setText("Tambah");

                clearForm();
                ShowTable();
            }

        } catch (ExceptionUsermame U) {
            JOptionPane.showMessageDialog(this, U.showMessageError());
            this.show();
        } catch (ExceptionPassword PE) {
            JOptionPane.showMessageDialog(this, PE.showMessageError());
            this.show();
        }


    }//GEN-LAST:event_btnTambahActionPerformed

