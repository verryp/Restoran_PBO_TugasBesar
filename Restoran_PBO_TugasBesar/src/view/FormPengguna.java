    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        if (this.selectedRow == null) {
            JOptionPane.showMessageDialog(null, "Silahkan pilih data yang hendak di hapus");
        } else {
            String nama = tblPengguna.getValueAt(this.selectedRow, 1).toString();
            int option = JOptionPane.showConfirmDialog(null, "Yakin Hapus data?", "Hapus data " + nama, JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (option == JOptionPane.OK_OPTION) {
                PC.deletePengguna(this.selectedId);
                ShowTable();
            } else if (option == JOptionPane.CANCEL_OPTION) {
                ShowTable();
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

