    public void ShowTable() {
        this.selectedRow = null;
        this.selectedId = null;
        tblPengguna.setModel(PC.showAll());
    }

    public void selectedDataPengguna() {
        this.selectedRow = tblPengguna.getSelectedRow();
        this.selectedId = Integer.parseInt(tblPengguna.getValueAt(selectedRow, 0).toString());
    }

    public void setEditableForm() {
        for (Pengguna p : PC.showById(this.selectedId)) {
            txtNama.setText(p.getNama());
            txtNamaPengguna.setText(p.getUsername());
            txtKataSandi.setText(p.getPassword());
            cmbPeran.setSelectedIndex(p.getPeran().getId());
        }
        btnTambah.setText("Simpan");
    }

    public void clearForm() {
        txtNama.setText("");
        txtNamaPengguna.setText("");
        txtKataSandi.setText("");
        cmbPeran.setSelectedIndex(0);
    }

