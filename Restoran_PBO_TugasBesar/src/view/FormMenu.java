    private void setEditableForm() {
        try{
        Integer id = Integer.parseInt(tblMenu.getValueAt(selectedRow, 0).toString());
        for (Menu m : MC.showById(id)) {
            txtNama.setText(m.getNama());
            txtHarga.setText(String.valueOf(m.getHarga()));
            txtDeskripsi.setText(m.getDeskripsi());
        }
        btnTambah.setText("Simpan");
        }catch(NullPointerException e){
            JOptionPane.showMessageDialog(null, "Anda belum memilih data");
        }
    }
    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        nama = txtNama.getText();
        harga = Integer.parseInt(txtHarga.getText());
        deskripsi = txtDeskripsi.getText();

        M = new model.Menu(MC.getNextID(), nama, harga, deskripsi);

        try {
            if (btnTambah.getText() == "Tambah") {
                //Enable();

                MC.insertMenu(M);
                clearForm();
                JOptionPane.showMessageDialog(this, "data berhasil diinputkan");
                ShowTable();
                checkHarga();
                
            } else if (btnTambah.getText() == "Simpan") {
                Integer id = Integer.parseInt(tblMenu.getValueAt(selectedRow, 0).toString());
                M.setID(id);

                MC.UpdateMenu(M);
                btnTambah.setText("Tambah");
                checkHarga();
                ShowTable();
                clearForm();
            }
            setEnPanelAdd(true);

        } catch (ExceptionHarga e) {
            JOptionPane.showMessageDialog(this, e.showMessageError());
            this.show();
        }


    }//GEN-LAST:event_btnTambahActionPerformed

    private void checkHarga() throws ExceptionHarga
    {
        if(Double.parseDouble(txtHarga.getText())<0)
        {
            throw new ExceptionHarga();
        }
    }
