    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        if (this.selectedRow == null) {
            JOptionPane.showMessageDialog(null, "Silahkan pilih data yang hendak di hapus");
        } else {
            Integer idDelete = Integer.parseInt(tblMenu.getValueAt(this.selectedRow, 0).toString());
            String menuDelete = tblMenu.getValueAt(this.selectedRow, 1).toString();
            int option = JOptionPane.showConfirmDialog(null, "Yakin hapus data?", "Hapus data " + menuDelete, JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (option == JOptionPane.OK_OPTION) {
                MC.deleteMenu(idDelete);
                ShowTable();
            } else if (option == JOptionPane.CANCEL_OPTION) {

                ShowTable();

            }
        }

    }//GEN-LAST:event_btnHapusActionPerformed

